public class operators {
    public static void main(String[] args) {
        // ARITHMETIC OPERATORS
        int a = 10;
        int b = 5;
        // to print something in same line we use print function

        System.out.print("The sum of a and b is ");
        System.out.print(a + b);

        System.out.println("");

        System.out.print("The subtraction of a and b is ");
        System.out.print(a - b);

        System.out.println("");

        System.out.print("The multiplication of a and b is ");
        System.out.print(a * b);

        System.out.println("");

        System.out.print("The division of a and b is ");
        System.out.print(a / b);

        System.out.println("");

        System.out.print("The remainder of a and b is ");
        System.out.println(a % b);

        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a); 
    }
}
